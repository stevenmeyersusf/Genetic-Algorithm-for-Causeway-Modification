      SUBROUTINE PRINT(VAR,VMASK,IM,JM,KNN,IVAR,S,IUNIT)
C     VERSION(03/02/90)
C
      DIMENSION VAR(IM,JM),IVAR(IM,JM),VMASK(IM,JM)
      DO 100 J=1,JM
      DO 100 I=1,IM
  100 IVAR(I,J)=IFIX(S*VAR(I,J))
      CALL MAXMIN(VAR,VMASK,IM,JM,VMAX,VMIN)
      KN=KNN
      IF(KN.GT.25) KN=25
      IB=1
      IE=KN
      NUM=(IM-1)/KN
      IF(IM.LE.KN) NUM=1
      IF(IM.LE.KN) IE=IM
      DO 200 K=1,NUM
      IF(K.EQ.1) WRITE(IUNIT,110) S,VMAX,VMIN
  110 FORMAT(//2X,'ACTUAL VALUE = PRINTED VALUE/',1PE9.2,
     .      5X,'MAX VALUE = ',1PE9.2,2X,'MIN VALUE = ',1PE9.2/)
      WRITE(IUNIT,120) (I,I=IB,IE)
  120 FORMAT(/4X,25I5,/)
      WRITE(IUNIT,130)
  130 FORMAT(/)
      DO 150 J=1,JM
      JJ=JM+1-J
      WRITE(IUNIT,140) JJ,(IVAR(I,JJ),I=IB,IE),JJ
  140 FORMAT(' ',I2,1X,25I5,I3)
  150 CONTINUE
      IB=IB+KN
      IE=IE+KN
  200 CONTINUE
      IF(IM.LE.KN) GO TO 300
      IE=IM
      WRITE(IUNIT,120) (I,I=IB,IE)
      WRITE(IUNIT,130)
      DO 250 J=1,JM
      JJ=JM+1-J
      WRITE(IUNIT,140) JJ,(IVAR(I,JJ),I=IB,IE),JJ
  250 CONTINUE
  300 RETURN
      END
      
